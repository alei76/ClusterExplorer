	@Test
	//tests if it generates clusters, and if they are contained inside the convex hull of the
	//entire point dataset
	public void testGetClusters() throws Exception
	{		

		try {
			double[][] tweets = createLargeTweetBlock();
			RenderableLayer layer=new RenderableLayer();
			layer.setName("DBSCAN Clusters");			
			RenderableLayer layer2=new RenderableLayer();
			layer2.setName("Level Labels");
			
			OpticsAlgorithm.ALG alg;
			Random rnd = new Random();
			alg=rnd.nextInt(2) < 1? OpticsAlgorithm.ALG.DBSCAN: OpticsAlgorithm.ALG.OPTICS;
			
			System.out.println(alg==OpticsAlgorithm.ALG.DBSCAN? "DBSCAN": "OPTICS");
			
			ArrayList<Polygon> clusters=OpticsAlgorithm.getClusters(tweets,layer,layer2,alg);
			
			assertTrue(clusters.size()>0);
			
			System.out.println("Found " + clusters.size() + " clusters");
			
			Coordinate[] coords=new Coordinate[tweets.length];
			
			for (int i=0; i <  tweets.length; ++i){
				coords[i]=new Coordinate(tweets[i][0],tweets[i][1]);
			}

			GeoPolygon poly=GeoPolygon.getConvexBoundary(coords);
			GeometryFactory gf=new GeometryFactory();
			com.vividsolutions.jts.geom.Polygon aPoly=gf.createPolygon(poly.getCoordinates().getCoordinates());
						
			Iterator<Polygon> itr = clusters.iterator();

		    while(itr.hasNext()){
		    	GeoPolygon tmpPoly=GeoPolygon.WW2geoPolygon(itr.next());
		    	
				com.vividsolutions.jts.geom.Polygon bPoly=
						gf.createPolygon(tmpPoly.getCoordinates().getCoordinates());
				
				assertFalse(aPoly.intersection(bPoly).isEmpty());
				assertTrue(aPoly.contains(bPoly));
				assertTrue(aPoly.intersects(bPoly));				
		    }					
			
		} catch (ParseException e) {
			// TODO Auto-generated catch block
			e.printStackTrace();
		}
		
	}

